// Question 22 using Switch (Check Consonant or Vowel using Switch)

function checkVowel(alpha){
    switch(alpha){
        case "a":
            console.log(alpha,"is a vowel")
            break
        case "e":
            console.log(alpha,  "is a vowel")
            break
        case "i":
            console.log(alpha, "is a vowel")
            break
        case "o":
            console.log(alpha, "is a vowel")
            break
        case "u":
            console.log(alpha, "is a vowel")
            break
        default:
            console.log(alpha, "is a consonant")
    }
}
checkVowel("e")